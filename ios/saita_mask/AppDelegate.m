static void ClearKeychainIfNecessary() {
    // Checks wether or not this is the first time the app is run
    if ([[NSUserDefaults standardUserDefaults] boolForKey:@"HAS_RUN_BEFORE"] == NO) {
        // Set the appropriate value so we don't clear next time the app is launched
        [[NSUserDefaults standardUserDefaults] setBool:YES forKey:@"HAS_RUN_BEFORE"];

        NSArray *secItemClasses = @[
            (__bridge id)kSecClassGenericPassword,
            (__bridge id)kSecClassInternetPassword,
            (__bridge id)kSecClassCertificate,
            (__bridge id)kSecClassKey,
            (__bridge id)kSecClassIdentity
        ];

        // Maps through all Keychain classes and deletes all items that match
        for (id secItemClass in secItemClasses) {
            NSDictionary *spec = @{(__bridge id)kSecClass: secItemClass};
            SecItemDelete((__bridge CFDictionaryRef)spec);
        }
    }
}

@implementation AppDelegate

- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
  ClearKeychainIfNecessary();
  RCTAppSetupPrepareApp(application);

  RCTBridge *bridge = [[RCTBridge alloc] initWithDelegate:self launchOptions:launchOptions];

#if RCT_NEW_ARCH_ENABLED
  _contextContainer = std::make_shared<facebook::react::ContextContainer const>();
  _reactNativeConfig = std::make_shared<facebook::react::EmptyReactNativeConfig const>();
  _contextContainer->insert("ReactNativeConfig", _reactNativeConfig);
  _bridgeAdapter = [[RCTSurfacePresenterBridgeAdapter alloc] initWithBridge:bridge contextContainer:_contextContainer];
  bridge.surfacePresenter = _bridgeAdapter.surfacePresenter;
#endif
  [FIRApp configure];
  
  UIView *rootView = RCTAppSetupDefaultRootView(bridge, @"saita_mask", nil);

  if (@available(iOS 13.0, *)) {
    rootView.backgroundColor = [UIColor blackColor];
  } else {
    rootView.backgroundColor = [UIColor blackColor];
  }
  

  

  self.window = [[UIWindow alloc] initWithFrame:[UIScreen mainScreen].bounds];
  UIViewController *rootViewController = [UIViewController new];
  rootViewController.view = rootView;
  self.window.rootViewController = rootViewController;
  [self.window makeKeyAndVisible];
  
  self.preventScreenShotModule = [PreventScreenShotModule allocWithZone:nil];
  [NSNotificationCenter.defaultCenter
      addObserverForName:(NSNotificationName)UIApplicationUserDidTakeScreenshotNotification
                  object:nil
                   queue:nil
              usingBlock:^(NSNotification *) {
    NSLog(@"Screenshot was taken %d",true);
    [self.preventScreenShotModule sendScreenshotEvent];
    
  }];
  
  CustomSecurityMethods * securityCheck = [[CustomSecurityMethods alloc] init];
      BOOL isJail = [securityCheck isJailBroken];
      
      if (isJail){
        UIAlertController* alert = [UIAlertController alertControllerWithTitle:@"SaitaPro"
                                       message:@"Your device is rooted. You cannot proceed."
                                       preferredStyle:UIAlertControllerStyleAlert];

        UIAlertAction* defaultAction = [UIAlertAction actionWithTitle:@"OK" style:UIAlertActionStyleDefault
           handler:^(UIAlertAction * action) {
          exit(0);}];

        [alert addAction:defaultAction];
        [rootViewController presentViewController:alert animated:YES completion:nil];
        return false;
      }
  
  
  
  return YES;
}


